    private void jPasswordField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPasswordField1ActionPerformed
        // Get the password entered by the user
    String password = new String(jPasswordField1.getPassword());

    // Validate the password length
    if (password.isEmpty()) {
        // Alert the user if the password field is empty
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Password field cannot be empty. Please enter a password.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } 
    else if (password.length() < 8) {
        // Alert the user if the password is less than 8 characters
        javax.swing.JOptionPane.showMessageDialog(this, 
            "The password must be at least 8 characters long.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } 
    else {
        // Password is valid
        System.out.println("Password entered: " + password);
        // Optionally display a success message
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Password accepted.", 
            "Success", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_jPasswordField1ActionPerformed

    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        // Get the email address entered by the user
    String email = jTextField2.getText();

    // Regular expression for a basic email format validation
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";

    // Validate if the email matches the regular expression
    if (email.isEmpty()) {
        // Alert the user if the email field is empty
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Email address cannot be empty. Please enter your email.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } else if (!email.matches(emailRegex)) {
        // Alert the user if the email format is invalid
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Please enter a valid email address.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } else {
        // Email is valid, proceed with your logic (e.g., store it, use it)
        System.out.println("Email entered: " + email);
        // Optionally, show a success message
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Email address accepted.", 
            "Success", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_jTextField2ActionPerformed

