    private void jTextField4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField4ActionPerformed
        // Get the mobile number entered by the user
    String mobileNumber = jTextField4.getText();

    // Validate if the mobile number is exactly 11 digits
    if (mobileNumber.length() != 11) {
        // Alert the user if the mobile number is not 11 digits
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Mobile number must be exactly 11 digits long.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } 
    // Check if the input contains only digits
    else if (!mobileNumber.matches("\\d+")) {
        // Alert the user if the input contains non-numeric characters
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Mobile number must contain only digits.", 
            "Input Error", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
    } else {
        // Optionally, process the mobile number (e.g., save it, print it)
        System.out.println("Mobile Number entered: " + mobileNumber);
        // You can display a success message if needed
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Mobile number accepted.", 
            "Success", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_jTextField4ActionPerformed

    private void jPasswordField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPasswordField2ActionPerformed
        // Get the password from the first and second password fields
    String password = new String(jPasswordField1.getPassword());  // Password entered in jPasswordField1
    String confirmPassword = new String(jPasswordField2.getPassword());  // Password entered in jPasswordField2

    // Validate if the passwords match
    if (!password.equals(confirmPassword)) {
        // Alert the user if the passwords don't match
        javax.swing.JOptionPane.showMessageDialog(this, 
            "The passwords do not match. Please confirm your password again.", 
            "Password Mismatch", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    } else {
        // Optionally, proceed if passwords match
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Passwords match!", 
            "Success", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }


    }//GEN-LAST:event_jPasswordField2ActionPerformed

